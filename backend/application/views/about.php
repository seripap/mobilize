		<h1 class="red">Our Services</h1>
		<p>We endeavor to challenge the area’s significant homeless LGBT youth problem in many ways. Some of our primary services include:</p>
		</br>
		<ul>
			<li>Answer phone, texts, and emails from LGBT homeless youth 24/7</li>
			<li>Meet face-to-face and ensure these youths have a roof over their head, clothes on their backs, and food in their bodies</li>
			<li>Have the youths evaluated by a mental health professional</li>
			<li>Connect the youths to HIV/STD testing</li>
			<li>Refer to health and dental services, if necessary</li>
			<li>Help obtain lost or stolen birth certificates, driver’s licenses or state ID cards</li>
			<li>Connect the youth to GED training/testing or other education resources</li>
			<li>Emergency and transitional housing</li>
			<li>Interview skills training</li>
		</ul>
